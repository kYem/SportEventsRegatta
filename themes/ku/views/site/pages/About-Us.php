<h1>About</h1>


<p>Henley Regatta was first held in 1839 and has been held annually ever since, except during the two World Wars. Originally staged by the Mayor and people of Henley as a public attraction with a fair and other amusements, the emphasis rapidly changed so that competitive amateur rowing became its main purpose.</p>

<p>The 1839 Regatta took place on a single afternoon but proved so popular with oarsmen that the racing lasted for two days from 1840. In 1886 the Regatta was extended to three days and to four in 1906. Since 1928 its increased popularity meant entries exceeded the permitted numbers in several events, and so Qualifying Races are now held in the week before the Regatta to reduce the number of entries to the permitted maximum. In 1986 the Regatta was extended to five days, with an increase in the maximum entry for certain events.
</p>


<p>In 1851 H.R.H. Prince Albert became the Regatta's first Royal Patron. Since the death of The Prince Consort, the reigning Monarch has always consented to become Patron. This patronage means the Regatta can be called Henley Royal Regatta.
</p>
<p>During the course of its history, the Regatta has often been honoured by visits of members of the Royal Family, of which the most recent was that of H.R.H. The Princess Royal in 2010.
 </p> 


